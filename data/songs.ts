export const getAllSongs = (): Song[] => {
  const bollywoodSongs = Object.values(BOLLYWOOD_ARTISTS).flat();
  const hollywoodSongs = Object.values(HOLLYWOOD_ARTISTS).flat();
  return [...bollywoodSongs, ...hollywoodSongs, ...SOUTH_INDIAN_SONGS];
};

export const searchSongs = (query: string): Song[] => {
  const allSongs = getAllSongs();
  const lowercaseQuery = query.toLowerCase();
  return allSongs.filter(
    (song) =>
      song.title.toLowerCase().includes(lowercaseQuery) ||
      song.artist.toLowerCase().includes(lowercaseQuery)
  );
};
